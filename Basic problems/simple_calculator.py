""" Write a program that prompts the user for two numbers and
displays the addition, subtraction, multiplication, and division
between them """

num1 = int(input("Enter number 1 : "))
num2 = int(input("Enter number 2 : "))
add = num1+num2
subtraction = num1-num2
multiplication = num1*num2
division = num1/num2
print(f"the addition of numbers {num1} and {num2} is : {add}")
print(f"the subtraction of numbers {num1} and {num2} is : {subtraction}")
print(f"the multiplication of numbers {num1} and {num2} is : {multiplication}")
print(f"The division of numbers {num1} and {num2} is: {division}")
